#!/usr/bin/env node
/**
 * Scans packages/opencode-worker/skills/* /SKILL.md and generates
 * src/skill/manifest.ts with static imports for each skill file.
 *
 * Run: node scripts/gen-skill-manifest.mjs
 */
import { readdirSync, existsSync, writeFileSync, readFileSync } from "fs"
import { join, dirname } from "path"
import { fileURLToPath } from "url"

const __dirname = dirname(fileURLToPath(import.meta.url))
const skillsDir = join(__dirname, "..", "skills")
const outFile = join(__dirname, "..", "src", "skill", "manifest.ts")

const dirs = existsSync(skillsDir)
  ? readdirSync(skillsDir, { withFileTypes: true })
      .filter((d) => d.isDirectory() && existsSync(join(skillsDir, d.name, "SKILL.md")))
      .map((d) => d.name)
      .sort()
  : []

const imports = dirs
  .map((name, i) => `import SKILL_${i} from "../../skills/${name}/SKILL.md"`)
  .join("\n")

const entries = dirs
  .map((name, i) => `  { dir: ${JSON.stringify(name)}, raw: SKILL_${i} },`)
  .join("\n")

const content = `// AUTO-GENERATED by scripts/gen-skill-manifest.mjs — do not edit
${imports}

export const skills = [
${entries}
]
`

const existing = existsSync(outFile) ? readFileSync(outFile, "utf-8") : ""
if (existing !== content) {
  writeFileSync(outFile, content, "utf-8")
  console.log(`[gen-skill-manifest] wrote ${dirs.length} skill(s) to src/skill/manifest.ts`)
} else {
  console.log(`[gen-skill-manifest] manifest up to date (${dirs.length} skill(s))`)
}
